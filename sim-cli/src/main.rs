use self::parsing::{create_simulation, Cli};
use clap::Parser;
use log::LevelFilter;
use simple_logger::SimpleLogger;

pub mod parsing;

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    // Enable tracing if building in developer mode.
    #[cfg(feature = "dev")]
    {
        console_subscriber::init();
    }

    let cli = Cli::parse();

    SimpleLogger::new()
        .with_level(LevelFilter::Warn)
        .with_module_level("simln_lib", cli.log_level)
        .with_module_level("sim_cli", cli.log_level)
        .init()
        .unwrap();

    let (sim, sim_network) = create_simulation(&cli).await?;
    let sim2 = sim.clone();
    ctrlc::set_handler(move || {
        log::info!("Shutting down simulation.");
        sim2.shutdown();
    })?;

    sim.run().await?;

    if let Some(network) = sim_network {
        network.lock().await.wait_for_shutdown().await;
    }

    Ok(())
}
